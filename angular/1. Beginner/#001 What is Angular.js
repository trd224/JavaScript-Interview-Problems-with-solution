// Angular is a front-end web framework used to build dynamic, modern web applications (like dashboards, admin panels, SPAs—Single Page Applications).

// It is developed and maintained by Google.

// 🔹 Simple Definition

// Angular is a TypeScript-based framework that helps developers build fast, structured, and scalable web apps.

// 🔹 Key Features

// 1. Component-Based Architecture
// Your UI is divided into small reusable pieces called components.
// Example: header, sidebar, login form, etc.

// 2. Two-Way Data Binding
// Data automatically syncs between UI (HTML) and logic (TypeScript).

// 3. Dependency Injection
// Helps manage services and makes code cleaner and testable.

// 4. Routing
// You can navigate between pages without reloading the browser.

// 5. Built-in Tools
// Forms, HTTP requests, validation, etc. are already included.



