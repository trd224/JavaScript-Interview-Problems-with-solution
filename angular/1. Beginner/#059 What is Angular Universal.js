// 🔹 What is Angular Universal?

// 👉 Angular Universal is a feature that enables server-side rendering (SSR) in Angular applications to render pages on the server instead of the browser.



// 🔹 Why It Is Used
// Improves performance (faster first load)
// Better SEO (search engines can read content)
// Improves user experience


// 🔹 How It Works
// Request comes from browser
// Server renders HTML using Angular
// Fully rendered page is sent to client
// Angular app then becomes interactive


// 🔹 Example Flow
// Client Request → Server (Angular Universal) → HTML Response → Browser