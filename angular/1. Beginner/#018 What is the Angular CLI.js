// 🔹 What is Angular CLI?

// Angular CLI (Command Line Interface) is a tool that helps you create, manage, and run Angular applications using simple commands.


// 🔹 Why Use Angular CLI?
// Create projects instantly
// Generate components/services automatically
// Run and build applications easily
// Follow best practices by default


// 🔹 Common Commands

// Create new project
// ng new my-app

// Run application
// ng serve

// Generate component
// ng generate component header
// or
// ng g c header

// Generate service
// ng g s data

// Build project
// ng build