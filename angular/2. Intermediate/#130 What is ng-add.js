// 👉 One-Line Interview Answer:
// ng add is an Angular CLI command used to install and automatically configure a library in an Angular project.


// 🔥 What is ng add?

// 👉 In simple words:
// It installs + sets up a library in one command.

// 👉 No manual configuration needed ✔



// 🔹 Example

// ng add @angular/material

// This will:

// Install package
// Add it to angular.json
// Import required modules
// Set up themes/styles


// 🔹 Without ng add ❌

// npm install @angular/material

// 👉 Then manually:

// Import modules
// Add styles
// Configure setup

// 😵 More work


// 🔹 With ng add ✅

// ng add @angular/material

// ✔ Everything auto-configured