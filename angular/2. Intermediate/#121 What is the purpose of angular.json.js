// 🔥 What is angular.json?

// 👉 In simple words:
// It is the main config file of Angular CLI that controls your project behavior.



// 🔹 What does it contain?

// 1. Project Configuration
// App name
// Source paths
// Output paths


// 2. Build Settings

// "build": {
//   "options": {
//     "outputPath": "dist/my-app",
//     "index": "src/index.html",
//     "main": "src/main.ts"
//   }
// }


// 3. Serve Configuration

// "serve": {
//   "options": {
//     "browserTarget": "my-app:build"
//   }
// }


// 4. Environment Config (file replacement)

// "fileReplacements": [
//   {
//     "replace": "environment.ts",
//     "with": "environment.prod.ts"
//   }
// ]


// 5. Assets, Styles, Scripts

// "assets": ["src/favicon.ico", "src/assets"],
// "styles": ["src/styles.css"],
// "scripts": []



