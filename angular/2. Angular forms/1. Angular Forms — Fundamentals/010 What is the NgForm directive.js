// NgForm is a directive in Angular that automatically creates and manages a form when we use a <form> tag in Template-Driven Forms.

// 👉 It provides:

// Tracks form state (valid, invalid, touched, dirty)
// Holds all form controls and their values
// Helps in form submission handling

// 👉 Example:

// <form #myForm="ngForm">
//   <input name="username" ngModel>
// </form>

// 👉 In short:
// NgForm represents the entire form and manages its state in template-driven forms.