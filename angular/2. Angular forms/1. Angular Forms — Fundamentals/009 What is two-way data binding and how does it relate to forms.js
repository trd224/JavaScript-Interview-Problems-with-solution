// Two-way data binding is a technique where data flows in both directions between the component (TypeScript) and the template (HTML).

// 👉 It means:

// When we update the input field → value updates in component
// When we update value in component → it updates in UI automatically

// 👉 In Angular forms, it is used like this:

// <input [(ngModel)]="username">

// 👉 Relation to forms:

// Used mainly in Template-Driven Forms
// Helps keep form input and component data in sync automatically

// 👉 In short:
// Two-way data binding keeps form data and component data synchronized.