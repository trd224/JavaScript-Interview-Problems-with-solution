// 👉 One-Line Interview Answer:
// A component library is a collection of reusable UI components that can be shared and used across multiple applications ( multiple projects).


// 🔥 What is a Component Library?

// 👉 In simple words:
// It is a set of ready-made UI components like:

// Buttons
// Forms
// Modals
// Cards

// 👉 We build once → reuse everywhere




// 🔹 Example

// Instead of creating button again and again:

// <button class="btn-primary">Save</button>

// 👉 We use:

// <app-button label="Save"></app-button>

// ✔ Reusable
// ✔ Consistent UI




// 🔹 Angular Example

// 👉 Create library:

// ng generate library ui-components

// 👉 Use in app:

// <ui-button></ui-button>
// <ui-card></ui-card>


// We use a component library to reuse the same components "across multiple projects", not just within a single application.



