// Interview Answer:

// Angular Material is a UI component library for Angular that provides pre-built, well-designed, and ready-to-use UI components following Material Design guidelines.


// 🔹 What it means (simple)
// It gives ready-made UI components
// You don’t need to build from scratch
// Components follow Google Material Design


// 🔹 Why we use it
// Faster development 🚀
// Consistent UI design 🎨
// Responsive and accessible components
// Less CSS work



// 🔹 Example components
// Buttons
// Inputs / Forms
// Tables
// Dialogs (popups)
// Side navigation
// Date picker


// 🔹 Example usage
// <button mat-button>Click Me</button>

// <mat-form-field>
//   <input matInput placeholder="Name">
// </mat-form-field>



// 🔹 Angular CDK vs Angular Material

// | Feature | Angular Material  | Angular CDK               |
// | ------- | ----------------- | ------------------------- |
// | UI      | Ready-made UI     | No UI (logic only)        |
// | Usage   | Quick development | Custom component building |
// | Design  | Material Design   | No design                 |
