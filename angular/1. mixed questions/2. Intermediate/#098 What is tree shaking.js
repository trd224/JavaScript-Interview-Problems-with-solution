// Interview Answer:

// Tree shaking is a build optimization process in Angular that removes unused (dead) code from the final application bundle to reduce file size and improve performance.



// 🔹 What it means (simple)
// Angular analyzes your code
// Finds unused functions, services, modules
// Removes them from final build



// 🔹 Why it is called “tree shaking”
// Your code is like a tree 🌳
// Unused code = dead branches
// Tree shaking = shaking tree to remove dead branches