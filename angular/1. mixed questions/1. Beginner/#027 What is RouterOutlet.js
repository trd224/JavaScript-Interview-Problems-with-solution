// 🔹 What is RouterOutlet in Angular?

// RouterOutlet is a directive that acts as a placeholder where Angular loads and displays routed components.



// 🔹 Syntax
// <router-outlet></router-outlet>


// 🔹 How It Works
// You define routes (URL → Component)
// User navigates to a route
// Angular loads that component
// Component is displayed inside <router-outlet>