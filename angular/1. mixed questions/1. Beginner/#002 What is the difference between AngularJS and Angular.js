// 🔹 Basic Difference
// AngularJS → Old framework (2010)
// Angular (2+) → Modern rewritten framework (2016+), maintained by Google
// 🔹 Key Differences
// 1. Language
// AngularJS → JavaScript
// Angular → TypeScript (strong typing, better for large apps)
// 2. Architecture
// AngularJS → MVC (Model-View-Controller)
// Angular → Component-based architecture

// 👉 Angular is more modular and clean.

// 3. Performance
// AngularJS → Slower (uses dirty checking)
// Angular → Faster (uses improved change detection)
// 4. Mobile Support
// AngularJS → Not optimized for mobile
// Angular → Fully optimized for mobile apps
// 5. Data Binding
// AngularJS → Two-way binding (heavy for performance)
// Angular → Two-way + One-way binding (more control)
// 6. Dependency Injection
// AngularJS → Basic
// Angular → Advanced and hierarchical DI system
// 7. Routing
// AngularJS → $routeProvider (limited)
// Angular → Powerful Router with lazy loading
// 8. Structure
// AngularJS → Less structured
// Angular → Highly structured (modules, components, services)
// 9. CLI Support
// AngularJS → No official CLI
// Angular → Comes with Angular CLI (powerful tool)



